package dto;

public class cdto {
	String no, model_name, car_num, car_made, car_made_ym, 
	auto_yn, opt_yn, acc_yn, fuel_ty, import_dt, rent_code;

	public cdto(String no, String model_name, String car_num, String car_made, String car_made_ym, String auto_yn,
			String opt_yn, String acc_yn, String fuel_ty, String import_dt, String rent_code) {
		super();
		this.no = no;
		this.model_name = model_name;
		this.car_num = car_num;
		this.car_made = car_made;
		this.car_made_ym = car_made_ym;
		this.auto_yn = auto_yn;
		this.opt_yn = opt_yn;
		this.acc_yn = acc_yn;
		this.fuel_ty = fuel_ty;
		this.import_dt = import_dt;
		this.rent_code = rent_code;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getModel_name() {
		return model_name;
	}

	public void setModel_name(String model_name) {
		this.model_name = model_name;
	}

	public String getCar_num() {
		return car_num;
	}

	public void setCar_num(String car_num) {
		this.car_num = car_num;
	}

	public String getCar_made() {
		return car_made;
	}

	public void setCar_made(String car_made) {
		this.car_made = car_made;
	}

	public String getCar_made_ym() {
		return car_made_ym;
	}

	public void setCar_made_ym(String car_made_ym) {
		this.car_made_ym = car_made_ym;
	}

	public String getAuto_yn() {
		return auto_yn;
	}

	public void setAuto_yn(String auto_yn) {
		this.auto_yn = auto_yn;
	}

	public String getOpt_yn() {
		return opt_yn;
	}

	public void setOpt_yn(String opt_yn) {
		this.opt_yn = opt_yn;
	}

	public String getAcc_yn() {
		return acc_yn;
	}

	public void setAcc_yn(String acc_yn) {
		this.acc_yn = acc_yn;
	}

	public String getFuel_ty() {
		return fuel_ty;
	}

	public void setFuel_ty(String fuel_ty) {
		this.fuel_ty = fuel_ty;
	}

	public String getImport_dt() {
		return import_dt;
	}

	public void setImport_dt(String import_dt) {
		this.import_dt = import_dt;
	}

	public String getRent_code() {
		return rent_code;
	}

	public void setRent_code(String rent_code) {
		this.rent_code = rent_code;
	}
	
	
	
	







	
	
	
	

}
